-- tcflags = tokenize cflags
-- 1500 iq for naming
-- This is probably one of the most absolute bajoding parts of the project
local function tcflags()
	if flags.cflags ~= "-Ofast -flto" then
		cflags = flags.cflags
	end

	local out = {}

	for flag in string.gmatch(cflags, "%S+") do
		out[#out+1] = '"' .. flag .. '"'
	end

	tcflags = table.concat(out, ", ")end

local function finishnob()
	print("Finishing nob.c...")
        local nob = io.open("nob.c", "a+")
        io.output(nob)
        io.write("\n")
	io.write("#define BUILD_FOLDER " .. build .. "\n")
	io.write("#define SRC_FOLDER " .. src .. "\n")

	io.write("\n")

	io.write("int main(int argc, char **argv)\n")
	io.write("{\n")

	io.write("	NOB_GO_REBUILD_URSELF(argc, argv);\n")
	-- I know this isn't needed with Reimu, but I'm not sure if it's nessisary to have or not.
	io.write("	if (!nob_mkdir_if_not_exists(BUILD_FOLDER)) return 1;\n")
	io.write("	Nob_Cmd cmd = {0};\n")

	-- This is the special mojo sauce line
	-- Get all the sauce you need!
	if wide == true then
		io.write('	nob_cmd_append(&cmd, "' .. compiler .. '", "-D_WCHAR", ' .. tcflags .. ', "-o", BUILD_FOLDER"ucvim", SRC_FOLDER"ucvim.c");\n')
	else
		io.write('	nob_cmd_append(&cmd, "' .. compiler .. '", ' .. tcflags .. ', "-o", BUILD_FOLDER"ucvim", SRC_FOLDER"ucvim.c");\n')
	end
	io.write("	if (!nob_cmd_run(&cmd)) return 1;\n")
	io.write("	return 0;\n")

	io.write("}\n")

        io.close(nob)
end

local function shscript()
	if flags.shell == true then
		print("Creating compile.sh")
		local sh = io.open("compile.sh", "a+")
		io.output(sh)
		io.write("#!/bin/sh\n")
		io.write("\n# Generated by Reimu by vmunix. https://github.com/vmunix486\n")
		io.write(compiler .. " -o nob nob.c && ./nob\n")
		io.close(sh)
		os.execute("chmod +x ./compile.sh")
	end
end

local function compile()
	if flags.compile == true and flags.cross == true then
		print("")
		print("Sorry, but you have to compile nob.c with your system compiler manually.")
		print("When you set --compiler and --cross, it sets the same variable to everything for the compiler,")
		print("which is usually expected to make code for the same machine that you are compiling on,")
		print("and not cross compilation.")
		print("")
		print("Sorry for the inconveniance. You can figure it out on your own if you want.")
		print("Pull Requests are free and greatly appreciated.")
		return
	end
	if flags.compile == true then
		print("Compiling nob...")
		os.execute(compiler .. " " .. cflags .. " " .. " nob.c -o nob") -- WHY LUA WHY!?!??!?!?!?!?!?!
		print("Running nob...")
		os.execute("./nob")
	end
end

