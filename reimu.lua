--[[
--	Command line argument parsing
--]]
local function parseargs(args)
	local parsed = {}
	for i = 1, #args do
		local arg = args[i]
		local key, value = arg:match("^%-%-([^=]+)=(.+)$") -- I need to look up how this works KEKW
		if key and value then
			parsed[key] = value
		elseif arg:sub(1, 2) == "--" then
			parsed[arg:sub(3)] = true
		else
			table.insert(parsed, arg)
		end
	end
	return parsed
end

local flags = {
	compiler = "cc",
	cflags = "-Ofast -flto",
	downloader = "wget",
	help = "false",
	clean = "false",
	compile = "false",
	shell = "false",
	cross = "false",
}

local args = parseargs(arg)

for k, v in pairs(args) do
	if flags[k] ~= nil then
		flags[k] = v
	end
end

local function help()
	if flags.help == true then
		print("Reimu is a nob configurator in Lua.")
		print("")
		print("Flags:")
		-- The tabs are all aligned correctly. Don't worry.
		print(" --compiler=[COMMAND]	The command to use as the C compiler command. (default: cc)")
		print(' --cflags=[FLAGS]	The flags to use for the C compiler flags. (default: "-Ofast -flto"')
		print(" --downloader=[COMMAND]	The command to use as the downloader (default: wget)")
		print(" --compile		Automatically compiles and runs nob to compile after configuring.")
		print(" --cross		Uses specified compiler as if it was a cross compiler. Skips the testing. Does not work with --compile.")
		print(" --shell		Make a shell script equivelant to --compile. Deprecated in favor of --compile.")
		print(" --clean		Cleans out all the files generated. Useful for git commits.")
		print(" --help			Prints this help dialog.")
		os.exit(0)
	end
end

local function clean()
	if flags.clean == true then
		os.execute("rm -rfv nob build/ nob.c nob.h compile.sh src/ucvim")
		os.exit(0)
	end
end

local function cross()
	if flags.cross == true then
		cross = true
	else
		cross = false
	end
end

local function flagmain()
	help()
	clean()
	cross()
end

